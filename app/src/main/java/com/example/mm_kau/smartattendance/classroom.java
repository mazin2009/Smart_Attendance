public class classroom {

    private String ID;
    private String Name;
    private String Cap;

    public void setID(String ID) {
        this.ID = ID;
    }

    public void setName(String name) {
        Name = name;
    }

    public void setCap(String cap) {
        Cap = cap;
    }

    public String getID() {
        return ID;
    }

    public String getName() {
        return Name;
    }

    public String getCap() {
        return Cap;
    }
}
